function createCard(){
    
    // Creation de la division qui va contenir la card
    const section = document.querySelector("section");
    const h1 = document.createElement("h1");
    section.appendChild(h1);
    h1.innerHTML = "Ma collection des films";
    const pInfo =document.createElement("p");
    section.appendChild(pInfo);
    pInfo.innerHTML = "Vous trouverez sur cette page une partie de ma collection des films. Cette collection va continuer à être enrichie avec le temps.";
    const div = document.createElement("div");
    div.id ="container";
    section.appendChild(div);
    for(const item of collection){
        const divCard = document.createElement("div");
        div.appendChild(divCard);
        // Creation de balise pour l'image
        const image = document.createElement("img");
        divCard.appendChild(image);
        let srcImg = item.photo;
        image.setAttribute("src", srcImg);
        // Creation de balise pour le nom du film
        const nom = document.createElement("h2");
        divCard.appendChild(nom); 
        nom.innerHTML = item.nom;  
        // Creation de balise pour le realisateur
        const realisateur = document.createElement("h3");
        divCard.appendChild(realisateur); 
        realisateur.id = "realisateur";
        realisateur.innerHTML = item.realisateur; 
        // Creation de balise pour le genre
        const genre = document.createElement("h4");
        divCard.appendChild(genre);
        genre.innerHTML = "Genre: " + item.genre;
        // Creation de balise pour les acteurs
        const acteur = document.createElement("h3");
        divCard.appendChild(acteur);
        acteur.id = "acteur";
        acteur.innerHTML = "Casting: " + item.acteurs;
        // Creation de balise pour l'année de production
        const anneeProd = document.createElement("h5");
        divCard.appendChild(anneeProd);
        anneeProd.innerHTML = "L'année de production: " + item.anneeProduction;
        // Creation de balise pour le résumé
        const resume = document.createElement("p");
        divCard.appendChild(resume);
        resume.innerHTML = item.resume;
        const bandeAnnonce = document.createElement("iframe");
        divCard.appendChild(bandeAnnonce);
        let srcVideo = item.bandeAnnonce;
        bandeAnnonce.setAttribute("src", srcVideo);
        bandeAnnonce.innerHTML = item.bandeAnnonce;
    }
}
createCard();
